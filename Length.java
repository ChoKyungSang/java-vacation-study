package array;

public class Length {

	public static void main(String[] args) {
		String[] classGroup = new String[4];
		classGroup[0] = "조경상";
		System.out.println(classGroup.length);
		classGroup[1] = "짱구";
		System.out.println(classGroup.length);
		classGroup[2] = "도라에몽";
		System.out.println(classGroup.length);
		classGroup[3] = "이고잉";
		System.out.println(classGroup.length);

	}

}
